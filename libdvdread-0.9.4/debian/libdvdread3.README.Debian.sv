Många DVD-filmer använder css.  För att se dessa behövs ett bibliotek,
libdvdcss.  Debian kan inte distribuera detta på grund av juridiska
problem, men det finns tillgängligt på andra platser på internet.  Om
det är lagligt för dig kan du köra skriptet
'/usr/share/doc/libdvdread3/examples/install-css.sh' för att hämta och
installera libdvdcss.
